package com.quest.practice_vars;

public class TypeCastingExamples {
    public static void main(String[] args) {
        int n=5;
        double a=n;
        System.out.println(a);

        double c=9.78;
        int n1= (int)c;
        System.out.println(n1);

        int n2=200;
        byte b= (byte)200;
        System.out.println(b);
    }
}
